// 'prompt' is used take user input.
/* Prompt is a modal in javascript. Modals are those popups which don't allow the user to interact with the other part of the 
webpage unless the user interacts with modal popup. */
let num; // considering the variable 'num'
num = prompt("Enter a number to continue!"); // assigning the value entered by the user in the prompt to num
console.log(num); // the entered number is logged in the console